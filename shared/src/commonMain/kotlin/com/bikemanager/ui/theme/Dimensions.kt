package com.bikemanager.ui.theme

import androidx.compose.ui.unit.dp

/**
 * Standardized dimensions for the "Premium Dark Automotive" design system.
 * All spacing, sizing, and radius values are defined here for consistency.
 */
object Dimens {
    // ========== Spacing ==========
    /** 4dp - Minimal spacing between tightly related elements */
    val SpaceXs = 4.dp

    /** 6dp - Compact internal padding */
    val SpaceSm = 6.dp

    /** 8dp - Standard spacing between cards in lists */
    val SpaceMd = 8.dp

    /** 10dp - Gap within rows */
    val SpaceBase = 10.dp

    /** 12dp - Internal card margins */
    val SpaceLg = 12.dp

    /** 16dp - Section padding, FAB margins */
    val SpaceXl = 16.dp

    /** 20dp - Screen horizontal padding */
    val Space2xl = 20.dp

    /** 24dp - Modal/dialog padding */
    val Space3xl = 24.dp

    // ========== Border Radius ==========
    /** 8dp - Small badges, chips */
    val RadiusSm = 8.dp

    /** 12dp - Buttons, inputs, small cards */
    val RadiusMd = 12.dp

    /** 16dp - Medium cards, modal internals */
    val RadiusLg = 16.dp

    /** 20dp - Large cards (BikeCard) */
    val RadiusXl = 20.dp

    /** 28dp - Bottom sheets */
    val Radius2xl = 28.dp

    /** 9999dp - Circular elements, pills */
    val RadiusFull = 9999.dp

    // ========== BikeCard Sizes ==========
    /** 48dp - Icon wrapper size in BikeCard */
    val BikeCardIconSize = 48.dp

    /** 14dp - Icon wrapper corner radius */
    val BikeCardIconRadius = 14.dp

    /** 40dp - Edit button size */
    val BikeCardEditBtnSize = 40.dp

    /** 40dp - Delete button size */
    val BikeCardDeleteBtnSize = 40.dp

    // ========== MaintenanceCard Sizes ==========
    /** 44dp - Icon wrapper size in MaintenanceCard */
    val MaintenanceCardIconSize = 44.dp

    /** 32dp - Check indicator size for todo items */
    val MaintenanceCheckSize = 32.dp

    // ========== FAB ==========
    /** 60dp - Floating action button size */
    val FabSize = 60.dp

    /** 18dp - FAB corner radius */
    val FabRadius = 18.dp

    /** 20dp - FAB right margin */
    val FabMargin = 20.dp

    /** 30dp - FAB bottom offset from screen edge */
    val FabBottomOffset = 30.dp

    // ========== Header & Navigation ==========
    /** 200dp - Parallax header height */
    val HeaderHeight = 200.dp

    /** 44dp - Back button size */
    val BackButtonSize = 44.dp

    // ========== Input & Button ==========
    /** 56dp - Input field height */
    val InputHeight = 56.dp

    /** 56dp - Primary button height */
    val ButtonHeight = 56.dp

    // ========== Tabs ==========
    /** 48dp - Tab height */
    val TabHeight = 48.dp

    /** 20dp - Tab count badge minimum width */
    val TabCountMinWidth = 20.dp
}
